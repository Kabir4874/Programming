#include <stdio.h>
int main()
{
    int n = 0, k = 0;
    char ch[10];
    while (k != 3)
    {
        gets(ch);
        if (ch[0] == '-' && ch[1] == '-' && ch[2] == '-')
        {
            n += 0;
        }
        if (ch[0] == '-' && ch[1] == '-' && ch[2] == '*')
        {
            n += 1;
        }
        if (ch[0] == '-' && ch[1] == '*' && ch[2] == '-')
        {
            n += 2;
        }
        if (ch[0] == '-' && ch[1] == '*' && ch[2] == '*')
        {
            n += 3;
        }
        if (ch[0] == '*' && ch[1] == '-' && ch[2] == '-')
        {
            n += 4;
        }
        if (ch[0] == '*' && ch[1] == '-' && ch[2] == '*')
        {
            n += 5;
        }
        if (ch[0] == '*' && ch[1] == '*' && ch[2] == '-')
        {
            n += 6;
        }
        if (ch[0] == '*' && ch[1] == '*' && ch[2] == '*')
        {
            n += 7;
        }
        if (ch[0] == 'c')
        {
            printf("%d\n", n);
            k++;
            n = 0;
        }
    }

    return 0;
}